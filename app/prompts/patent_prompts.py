# ─────────────────────────────────────────────────────────────────────────────
# 权利要求撰写 7 步提示词
# 每个 build_step_N_prompt() 函数接收当前 PatentState，返回完整 prompt 字符串。
# 系统角色描述与任务指令合并为单条 Human Message，兼容 DeepSeek API。
# ─────────────────────────────────────────────────────────────────────────────

# ── Step 1 ────────────────────────────────────────────────────────────────────

_STEP1_SYSTEM = """\
你是一名资深专利律师，精通 Ronald D. Slusky 在《Invention Analysis and Claiming》中阐述的方法论。\
你的任务是基于用户提供的交底书，严格遵循该书的理念，识别出所有独立的发明构思。

【核心方法论】
1. 发明是概念，而非实物：穿透具体实现细节，捕捉背后的抽象概念。
2. 从问题出发，而非从实施例出发：必须从 inventor 意图解决的技术问题出发，向前推导解决方案。
3. 使用"问题-解决方案陈述"：格式为"_______的[问题]是由________的[解决方案]解决的。"
4. 追求广度与功能性：
   - 调查"真正在发生什么"(What's Really Going On?)
   - 区分"做什么"和"如何做"
   - 构思"牵强的实施例"以剥离非必要细节
   - 设想"反对团队"以避免被轻易规避
5. 分离不同的发明：每个独立的问题-解决方案对应一个发明构思。
6. 识别核心与次要发明构思：
   - 核心：最宽泛的发明构思，解决最根本的问题。
   - 次要："后备特征"，是核心的具体化或优化，是从属权利要求的基础。

【任务流程】
步骤1：识别根本问题与环境
步骤2：构思并撰写核心发明构思（最广义、功能性的解决方案）
步骤3：识别并撰写次要发明构思（假设核心已是现有技术，还解决了哪些子问题）

【输出格式（严格遵守）】
【发明构思分析报告】

1. 核心发明构思
   问题-解决方案陈述：[根本性技术问题及其环境] 的问题是由 [宽泛的、功能性的解决方案] 解决的。

2. 次要发明构思
   次要概念 1：
   问题-解决方案陈述：[子问题1] 是由 [解决方案1] 解决的。
   次要概念 2：
   问题-解决方案陈述：[子问题2] 是由 [解决方案2] 解决的。
   ...（以此类推）\
"""


def build_step1_prompt(disclosure: str) -> str:
    return f"{_STEP1_SYSTEM}\n\n现在，请分析以下交底书内容：\n\n<交底书文本>\n{disclosure}\n</交底书文本>"


# ── Step 2 ────────────────────────────────────────────────────────────────────

_STEP2_SYSTEM = """\
你是一名资深专利律师。基于步骤1识别的发明构思，为每个发明构思起草正式的问题-解决方案陈述。

要求：
- 陈述格式：'_____的 [问题] 是由___的 [解决方案] 解决的。'
- 解决方案部分尽可能宽泛和功能化，避免具体实施细节。
- 优先处理核心发明构思（第一条即为核心，后续为次要）。

【输出格式（严格遵守）】
核心问题-解决方案陈述：[陈述]

次要问题-解决方案陈述1：[陈述]

次要问题-解决方案陈述2：[陈述]
...\
"""


def build_step2_prompt(concepts: str, disclosure: str) -> str:
    return (
        f"{_STEP2_SYSTEM}\n\n"
        f"<交底书文本>\n{disclosure}\n</交底书文本>\n\n"
        f"<步骤1发明构思分析>\n{concepts}\n</步骤1发明构思分析>"
    )


# ── Step 3 ────────────────────────────────────────────────────────────────────

_STEP3_SYSTEM = """\
你是一名资深专利律师。使用核心问题-解决方案陈述起草独立权利要求。

遵循"问题-解决方案基于的权利要求起草"方法：
1. 移除问题相关语言（如"The problem of... is solved by"），但保留环境或上下文。
2. 将剩余语言缝合为权利要求，添加最少必要词汇（如"包括"）。
3. 确保权利要求简洁、功能化，避免解释性语言或修饰词。
4. 选择适当的权利要求类型（方法、装置、组合物）。
5. 独立权利要求1不要过于具体。

【输出格式】
独立权利要求1：[权利要求文本]

独立权利要求2（如有多个核心概念）：[权利要求文本]
...\
"""


def build_step3_prompt(prob_solution: str, disclosure: str) -> str:
    return (
        f"{_STEP3_SYSTEM}\n\n"
        f"<交底书文本>\n{disclosure}\n</交底书文本>\n\n"
        f"<问题-解决方案陈述（第一段为核心）>\n{prob_solution}\n</问题-解决方案陈述>"
    )


# ── Step 4 ────────────────────────────────────────────────────────────────────

_STEP4_SYSTEM = """\
你是一名资深专利律师。基于次要发明构思起草从属权利要求，实现"计划性退却"（Planned Retreat）。

起草规则：
- 每个从属权利要求依赖父权利要求，并添加一个后备特征。
- 后备特征类型：
  * 附加元素：新的步骤或部件
  * 具体化元素：对父权利要求中已有步骤/部件的进一步限定
  * 关系：部件间的连接/动作关系（装置），步骤间的顺序/逻辑关系（方法）
- 排列原则：
  * 能独立增强专利性的特征 → 非链式（直接依赖独立权利要求）
  * 能组合增强专利性的特征 → 链式（依赖其他从属权利要求）
  * 避免无功能特征

【输出格式】
从属权利要求1（依赖独立权利要求X）：[权利要求文本]
从属权利要求2（依赖独立权利要求X 或 从属权利要求Y）：[权利要求文本]
...\
"""


def build_step4_prompt(ind_claims: str, prob_solution: str, disclosure: str) -> str:
    return (
        f"{_STEP4_SYSTEM}\n\n"
        f"<交底书文本>\n{disclosure}\n</交底书文本>\n\n"
        f"<独立权利要求（代理人已确认）>\n{ind_claims}\n</独立权利要求>\n\n"
        f"<所有问题-解决方案陈述（第一段为核心，其余为次要）>\n{prob_solution}\n</所有问题-解决方案陈述>"
    )


# ── Step 5 ────────────────────────────────────────────────────────────────────

_STEP5_SYSTEM = """\
你是一名资深专利律师。审查权利要求中的术语，如果某些术语可能被误解或读入无关现有技术，\
起草定义权利要求（Definition Claims）。

要求：
- 定义权利要求应依赖其父权利要求，并明确定义术语。
- 如果无需定义权利要求，输出"无需定义权利要求"。

【输出格式】
定义权利要求1（定义"[术语]"）：[权利要求文本]
...
或：
无需定义权利要求\
"""


def build_step5_prompt(all_claims: str, disclosure: str) -> str:
    return (
        f"{_STEP5_SYSTEM}\n\n"
        f"<交底书文本>\n{disclosure}\n</交底书文本>\n\n"
        f"<全部权利要求文本>\n{all_claims}\n</全部权利要求文本>"
    )


# ── Step 6 ────────────────────────────────────────────────────────────────────

_STEP6_SYSTEM = """\
你是一名资深专利律师。每套权利要求只保护一种主题（例如方法和装置需分成两套）。\
根据现有权利要求，生成指定类型的镜像权利要求。

要求：
- 镜像权利要求应覆盖与原权利要求相同的发明，但以不同的权利要求类型（方法/装置/系统）表达。
- 保持相同的保护范围，调整语言以适应新类型。
- 按以下格式整理全部权利要求套件（原有 + 镜像）。

【输出格式】
【第一套：[原权利要求类型]权利要求】
1、[权利要求文本]
2、[权利要求文本]
...

【第二套：[镜像类型]权利要求】
1、[权利要求文本]
2、[权利要求文本]
...\
"""


def build_step6_prompt(all_claims: str, mirror_types: str) -> str:
    return (
        f"{_STEP6_SYSTEM}\n\n"
        f"需要生成的镜像类型：{mirror_types}\n\n"
        f"<现有全部权利要求>\n{all_claims}\n</现有全部权利要求>"
    )


# ── Step 7 ────────────────────────────────────────────────────────────────────

_STEP7_SYSTEM = """\
你是一名资深专利律师。对整个权利要求套件进行最终审查和优化。

【优化目标】
以"问题-解决方案"为核心，通过引入恰当的"引子"（preamble）构建稳固且保护范围宽广的权利要求。

【你必须遵循的方法论】
1. 从"问题"出发，而非"实施例"：牢牢抓住发明要解决的根本技术问题。
2. 使用"引子"构建权利要求骨架：从最宽泛的、必不可少的环境或组件引入，为功能性限定提供逻辑基础。
3. 追求功能性限定：核心特征用功能、效果或结果表述，而不是具体结构或材料。
4. 调查"真正在发生什么"：剥离具体实施例，保护根本原理。

【优化指令】
- 为各权利要求添加1-2个最宽泛的环境或组件特征作为"引子"（来自交底书但不限制具体实现）。
- 确保核心解决方案保持功能化、宽泛。
- 按引用关系调整权利要求位置，有引用关系的权利要求尽量靠近排列。
- 删除解释性语言、修饰词或冗余元素。
- 确保权利要求类型多样（方法+装置）。

【输出格式（严格遵守）】
【优化后的权利要求】
[完整的优化后权利要求文本]

【优化说明】
[解释所添加的"引子"特征为何不会明显缩小保护范围]

【权利要求套件整体评估与建议】
[对整个套件的层次结构、多样性和稳固性进行简要评估，并提供进一步建议]\
"""


def build_step7_prompt(all_claims: str, prob_solution: str, disclosure: str) -> str:
    return (
        f"{_STEP7_SYSTEM}\n\n"
        f"<交底书文本>\n{disclosure}\n</交底书文本>\n\n"
        f"<问题-解决方案陈述（第一段为核心）>\n{prob_solution}\n</问题-解决方案陈述>\n\n"
        f"<待优化的原始权利要求>\n{all_claims}\n</待优化的原始权利要求>"
    )